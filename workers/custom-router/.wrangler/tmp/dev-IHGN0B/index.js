async function verifyLinearSignature(payload, signature, secret) {
  const encoder2 = new TextEncoder();
  const keyData = encoder2.encode(secret);
  const payloadData = encoder2.encode(payload);
  const key = await crypto.subtle.importKey(
    "raw",
    keyData,
    { name: "HMAC", hash: "SHA-256" },
    false,
    ["verify"]
  );
  const cleanSignature = signature.replace(/^sha256=/, "");
  const signatureBuffer = hexStringToBuffer(cleanSignature);
  return await crypto.subtle.verify(
    "HMAC",
    key,
    signatureBuffer,
    payloadData
  );
}
__name(verifyLinearSignature, "verifyLinearSignature");
function hexStringToBuffer(hexString) {
  const bytes = new Uint8Array(hexString.length / 2);
  for (let i = 0; i < hexString.length; i += 2) {
    bytes[i / 2] = parseInt(hexString.substring(i, i + 2), 16);
  }
  return bytes.buffer;
}
